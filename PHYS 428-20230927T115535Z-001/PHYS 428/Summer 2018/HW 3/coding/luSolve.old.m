for k=1:n
    for i=1:n
        for j=1:n
    y(1,1) =  b(1,1)/L(1,1);
    y(2,1) = (b(2,1) - L(2,1)*x(1,1))/L(2,2);
    y(3,1) = (b(3,1) - L(3,2)*x(2,1) - L(3,1)*x(1,1))/L(3,3);
    
        y(i,j)=b(i,1) - L(i,j-1)*x(i-1,j-);
        end
        y(i,j)=y(i,j)/L(i,j)
    end
end
